	MOVF	STATUS_TEMP,W
	MOVWF	STATUS
	SWAPF	W_TEMP,F
	SWAPF	W_TEMP,W
	BCF		INTCON,INTF
	RETFIE
MAIN
	BSF		STATUS,RP0
	BCF		INTCON,GIE
	MOVLW	B'00000001
	MOVWF	TRISB
	BCF		OPTION_REG,INTEDG
	BCF		STATUS,RP0
	BSF		INTCON,GIE
	BSF		INTCON,INTE		
MAINLP
	MOVF	OUT,W
	XORLW	B'00110000'
	BTFSC	SATUS,Z
	CLRF	PORTB

	MOVF	OUT,W
	XORLW	B'00110001'
	BTFSC	SATUS,Z
	CLRF	PORTB
	BTFSC	SATUS,Z
	BSF		PORTB,1

	MOVF	OUT,W
	XORLW	B'00110010'
	BTFSC	SATUS,Z
	CLRF	PORTB
	BTFSC	SATUS,Z
	BSF		PORTB,2

	MOVF	OUT,W
