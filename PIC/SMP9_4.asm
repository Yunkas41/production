	BTFSC	SATUS,Z
	CLRF	PORTB
	BTFSC	SATUS,Z
	BSF		PORTB,4

	MOVF	OUT,W
	XORLW	B'00110101'
	BTFSC	SATUS,Z
	CLRF	PORTB
	BTFSC	SATUS,Z
	BSF		PORTB,5
	GOTO    MAINLP		
ON
	MOVLW	B'00111110'
	MOVWF	PORTB
	RETURN
CRECV
	CLRF	TMPA
	MOVLW	08H
	MOVWF	CNTB
	CALL	TIME156
CRECVLP
	BTFSC	PORTB,0
	BSF		STATUS,C
	BTFSS	PORTB,0
	BCF		STATUS,C
	RRF		TMPA,F
	CALL	TIME104
	DECFSZ	CNTB,F
	GOTO	CRECVLP
	MOVF	TMPA,W
	RETURN
TIME156
	MOVLW	0C3H
	MOVWF	CNT1
LOOP1
	NOP
	DECFSZ	CNT1,F
	GOTO	LOOP1
	RETURN
